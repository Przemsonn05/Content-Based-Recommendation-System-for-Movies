def evaluate_baseline_model(df, top_n_movies):
    """
    Calculates and returns basic metrics for the baseline recommendation model.

    Args:
        df (pd.DataFrame): The entire movie dataset.
        top_n_movies (pd.DataFrame): The recommended movies generated by the baseline model.

    Returns:
        dict: A dictionary containing various evaluation metrics such as average rating, 
              genre diversity, year standard deviation, and popularity bias.
    """
    metrics = {}
    
    metrics['avg_rating'] = top_n_movies['vote_average'].mean()
    metrics['avg_vote_count'] = top_n_movies['vote_count'].mean()
    if 'weighted_rating' in top_n_movies.columns:
        metrics['avg_weighted_rating'] = top_n_movies['weighted_rating'].mean()
    
    all_genres = [g for genres in top_n_movies['genres'] for g in genres if isinstance(genres, list)]
    metrics['genre_diversity'] = len(set(all_genres)) if all_genres else 0
    if all_genres:
        metrics['most_common_genre'] = Counter(all_genres).most_common(1)[0]
    
    metrics['year_std'] = top_n_movies['release_year'].std()
    metrics['avg_year'] = top_n_movies['release_year'].mean()
    metrics['oldest_movie'] = top_n_movies['release_year'].min()
    metrics['newest_movie'] = top_n_movies['release_year'].max()
    
    metrics['coverage'] = (len(top_n_movies) / len(df)) * 100
    metrics['popularity_bias'] = top_n_movies['vote_count'].mean() / df['vote_count'].mean()
    
    return metrics

def evaluate_model(data, cosine_sim, indices, sample_size=50, top_k=10, alpha=0.3, plot_charts=False):
    """
    Evaluates the intelligent content-based recommendation model on a sample of movies.

    Args:
        data (pd.DataFrame): The movie dataset.
        cosine_sim (np.ndarray): The precomputed cosine similarity matrix.
        indices (pd.Series): A pandas Series mapping movie titles to their DataFrame indices.
        sample_size (int, optional): The number of movies to sample for evaluation. Defaults to 50.
        top_k (int, optional): The number of top recommendations to consider. Defaults to 10.
        alpha (float, optional): The weight parameter balancing quality and similarity. Defaults to 0.3.
        plot_charts (bool, optional): Whether to plot charts during evaluation. Defaults to False.

    Returns:
        dict: A dictionary containing averaged metrics across the sample: 
              quality, diversity, genre_overlap, and popularity_bias.
    """
    metrics = {'quality': [], 'diversity': [], 'genre_overlap': [], 'popularity_bias': []}
    
    safe_sample = min(sample_size, len(data))
    sample_indices = random.sample(list(data.index), safe_sample)
    avg_dataset_votes = data['vote_count'].mean()

    for idx in sample_indices:
        title = data.iloc[idx]['original_title']
        recs = recommendation(title, cosine_sim, data, indices, alpha=alpha, use_mmr=True)
        
        if recs is None or recs.empty: 
            continue
            
        recs = recs.head(top_k)
        
        metrics['quality'].append(recs['vote_average'].mean())
        metrics['popularity_bias'].append(recs['vote_count'].mean() / avg_dataset_votes)
        
        input_genres = set(data.iloc[idx].get('genres', []))
        overlaps = []
        for _, row in recs.iterrows():
            r_genres = set(row.get('genres', []))
            denom = len(input_genres | r_genres)
            overlaps.append(len(input_genres & r_genres) / denom if denom > 0 else 0)
        metrics['genre_overlap'].append(np.mean(overlaps) if overlaps else 0)

        rec_idxs = recs.index.tolist()
        if len(rec_idxs) > 1:
            pairs = list(itertools.combinations(rec_idxs, 2))
            pair_sims = [cosine_sim[p[0]][p[1]] for p in pairs]
            metrics['diversity'].append(1 - np.mean(np.clip(pair_sims, 0, 1)))
        else:
            metrics['diversity'].append(0)

    return {k: np.mean(v) if v else 0 for k, v in metrics.items()}


def precision_at_k(recommended, relevant, k):
    """Calculates Precision@K."""
    return sum(1 for t in recommended[:k] if t in relevant) / k

def recall_at_k(recommended, relevant, k):
    """Calculates Recall@K."""
    if not relevant: return 0
    return sum(1 for t in recommended[:k] if t in relevant) / len(relevant)

def ndcg_at_k(recommended, relevant, k):
    """Calculates Normalized Discounted Cumulative Gain (NDCG@K)."""
    dcg = sum(1 / np.log2(rank + 2) for rank, title in enumerate(recommended[:k]) if title in relevant)
    ndcg = sum(1 / np.log2(rank + 2) for rank in range(min(k, len(relevant))))
    return dcg / ndcg if ndcg > 0 else 0.0

def hit_rate_at_k(recommended, relevant, k):
    """Calculates Hit Rate@K (1 if any relevant item is in top K, 0 otherwise)."""
    return float(any(t in relevant for t in recommended[:k]))

def run_leave_one_out_evaluation(data_cleaned2, ratings, movies_from_rate, cosine_sim, indices, num_of_users=500, k_values=[5, 10], seed=42):
    """
    Performs a Leave-One-Out cross-validation on a sample of users to evaluate the recommender.

    Args:
        data_cleaned2 (pd.DataFrame): The preprocessed movie dataset.
        ratings (pd.DataFrame): The user ratings dataset.
        movies_from_rate (pd.DataFrame): The secondary movies dataset associated with ratings.
        cosine_sim (np.ndarray): The precomputed cosine similarity matrix.
        indices (pd.Series): A pandas Series mapping movie titles to their DataFrame indices.
        num_of_users (int, optional): The number of users to sample for evaluation. Defaults to 500.
        k_values (list, optional): A list of 'K' values to compute metrics at (e.g., [5, 10]). Defaults to [5, 10].
        seed (int, optional): The random seed for reproducibility. Defaults to 42.

    Returns:
        tuple: A tuple containing (summary_metrics DataFrame, detailed_results DataFrame).
    """
    print("Preparing data for Leave-One-Out validation...")
    
    movies_from_rate['clean_title'] = movies_from_rate['title'].str.replace(r'\s*\(\d{4}\)$', '', regex=True).str.strip()
    
    like_threshold = 3.5
    min_like = 10
    
    linked = ratings[ratings['rating'] >= like_threshold].merge(
        movies_from_rate[['movieId', 'clean_title']], on='movieId'
    )
    
    tmdb_titles = set(data_cleaned2['original_title'].values)
    linked = linked[linked['clean_title'].isin(tmdb_titles)]
    
    users = {
        str(ids): set(group['clean_title'].tolist())
        for ids, group in linked.groupby('userId') if len(group) >= min_like
    }

    rng = np.random.default_rng(seed)
    users_ids = list(users.keys())
    sampled = rng.choice(users_ids, size=min(num_of_users, len(users_ids)), replace=False)
    
    records = []
    print(f"Running validation on a sample of {len(sampled)} users...")

    for user_id in sampled:
        liked_titles = list(users[user_id])
        seed_title = rng.choice(liked_titles)
        relevant = set(liked_titles) - {seed_title}

        try:
            recs = recommendation(title=seed_title, cosine_similarity=cosine_sim, data=data_cleaned2, indices=indices)
            if recs is None or recs.empty:
                continue
            recommended = recs['original_title'].tolist()
        except Exception as e:
            continue

        row = {'user_id': user_id, 'seed_movie': seed_title}
        for k in k_values:
            row[f'precision@{k}'] = precision_at_k(recommended, relevant, k)
            row[f'recall@{k}'] = recall_at_k(recommended, relevant, k)
            row[f'ndcg@{k}'] = ndcg_at_k(recommended, relevant, k)
            row[f'hit_rate@{k}'] = hit_rate_at_k(recommended, relevant, k)
        records.append(row)

    results = pd.DataFrame(records)
    
    summary_rows = []
    for metric in ['precision', 'recall', 'ndcg', 'hit_rate']:
        row = {'Metric': metric}
        for k in k_values:
            col = f'{metric}@{k}'
            row[f'@{k} mean'] = results[col].mean()
            row[f'@{k} std']  = results[col].std()
        summary_rows.append(row)

    summary_metrics = pd.DataFrame(summary_rows).set_index('Metric')
    print("\n--- Evaluation Results (Leave-One-Out) ---")
    print(summary_metrics.to_string())
    
    return summary_metrics, results

def save_and_upload_models(data_cleaned2, cosine_sim, indices, C, m, hf_repo_id='Przemsonn/Recommendation_System'):
    """
    Prepares the model artifacts, saves them temporarily, and uploads them to the Hugging Face Hub.

    Args:
        data_cleaned2 (pd.DataFrame): The final preprocessed movie dataset.
        cosine_sim (np.ndarray): The precomputed cosine similarity matrix.
        indices (pd.Series): A pandas Series mapping movie titles to their DataFrame indices.
        C (float): The mean vote average across the dataset (used for weighted rating).
        m (float): The minimum votes required to be listed in the chart (used for weighted rating).
        hf_repo_id (str, optional): The target Hugging Face repository ID. Defaults to 'Przemsonn/Recommendation_System'.
    """
    hf_token = os.getenv("HF_TOKEN")
    if not hf_token:
        print("Error: HF_TOKEN environment variable not found. Skipping upload.")
        return

    print("Logging into Hugging Face...")
    login(hf_token)

    columns_to_keep = [
        'original_title', 'original_language', 'movie_age', 'vote_average', 
        'vote_count', 'genres', 'hybrid_score'
    ]
    existing_cols = [c for c in columns_to_keep if c in data_cleaned2.columns]
    data_for_app = data_cleaned2[existing_cols].copy()

    baseline_model = {
        'C': C, 'm': m, 'movies_df': data_cleaned2
    }
    model_data = {
        'dataframe': data_for_app, 'similarity': cosine_sim, 'indices': indices
    }

    with tempfile.TemporaryDirectory() as tmp:
        path1 = os.path.join(tmp, 'recommendation_intelligent_model.joblib')
        path2 = os.path.join(tmp, 'recommendation_baseline_model.joblib')
        
        print("Saving models to temporary files...")
        joblib.dump(model_data, path1)
        joblib.dump(baseline_model, path2)

        api = HfApi()
        print(f"Uploading the intelligent model to: {hf_repo_id}...")
        api.upload_file(
            path_or_fileobj=path1,
            path_in_repo='recommendation_intelligent_model.joblib',
            repo_id=hf_repo_id,
            repo_type='model'
        )

        print(f"Uploading the baseline model to: {hf_repo_id}...")
        api.upload_file(
            path_or_fileobj=path2,
            path_in_repo='recommendation_baseline_model.joblib',
            repo_id=hf_repo_id,
            repo_type='model'
        )
        print("Successfully uploaded to Hugging Face!")